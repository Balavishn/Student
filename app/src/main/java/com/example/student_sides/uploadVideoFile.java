package com.example.student_sides;
public class uploadVideoFile{
    public String name;
    public String url;

    public uploadVideoFile() {
    }

    public uploadVideoFile(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}

